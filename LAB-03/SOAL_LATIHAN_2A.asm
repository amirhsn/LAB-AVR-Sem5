.INCLUDE "M8535DEF.INC"
.ORG 0

RJMP MAIN

MAIN:	LDI R16, LOW(RAMEND)
		OUT SPL, R16
		LDI R16, HIGH(RAMEND)
		OUT SPH, R16

		//INISIALISASI COUNTER
		LDI R31, 0
		//INISIALISASI POINTER X
		LDI R26, $60
		LDI R27, 0
		LD R18, Z+
L11:	LD R19, Z+
		
		CP R19, R18
		BRLO TUKAR
L22:	INC R31
		CPI R31, 0X0A
		BRMI L11

AKHIR:	RJMP AKHIR

TUKAR:	CLR R18
		MOV R18, R19
		RJMP L22

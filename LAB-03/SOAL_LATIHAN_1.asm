.INCLUDE "M8535DEF.INC"
.ORG 0

RJMP MAIN

MAIN:	LDI R16, LOW(RAMEND)
		OUT SPL, R16
		LDI R16, HIGH(RAMEND)
		OUT SPH, R16

		//INISIALISASI COUNTER
		LDI R31, 0
		//INISIALISASI POINTER X
		LDI R26, 0
		LDI R27, 0
		LD R20, X+
L11:	LD R21, X+
		
		CP R20, R21
		BRLO TUKAR
L22:	INC R31
		CPI R31, 0X0A
		BRMI L11

AKHIR:	RJMP AKHIR

TUKAR:	CLR R20
		MOV R20, R21
		RJMP L22

.INCLUDE "M8535DEF.INC"
.ORG 0

RJMP MAIN
MAIN:
LDI R16, LOW(RAMEND)
OUT SPL, R16
LDI R16, HIGH(RAMEND)
OUT SPH, R16

//INISIALISASI POINTER
LDI ZL, LOW($70)
LDI ZH, HIGH($70)
//INISIALISASI COUNTER
LDI R19,0
LD R20, Z+
RCALL OPR

AKHIR: RJMP AKHIR

OPR:
LD R21, Z+
CP R20, R21
BRLO SKIP
MOV R20, R21
SKIP:
INC R19
CPI R19, $0A
BREQ AKHIR
RJMP OPR
